        <p>Following the devastating earthquake in 2010, Jonathan Nash Glynn founded Wings Over Haiti. Initially, it was little more than Jonathan––a pilot with a small airplane––doing what jets laden with relief supplies could not: flying in and out of Haiti, especially to remote regions, delivering much-needed medical supplies, food and aid. Jonathan partnered with Melissa McMullen, a middle school teacher and Chad St. Louis, a Haitian-American high school guidance counselor and together, they opened the first Wings Over Haiti school. Today, this vibrant facility has over 180 students from nursery to 7th grade and a faculty of over 25. It was precisely this experience of opening a fully sustainable school that prompted Magalie Theodore, a Haitian now living and working in New York City, to contact Jonathan and offer a gift. The Theodore family bequeathed a 7-acre plot in Ranquitte to Wings Over Haiti with the sole condition that the dilapidated school which once stood in the middle of the land, would be rebuilt and filled, once again, with more students. Please join us in our next adventure, giving hope to children who otherwise have none, and donate to our new school. With your help, these kids can soar.</p>
