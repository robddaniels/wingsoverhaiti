            <h3>Our Curriculum</h3>
            <p>In accordance with the Ministry of National Education and Vocational Training of Haiti, Wings Over Haiti follows the national curriculum implemented by the Ministry of Education. Beginning in pre-primary, students learn bilingually in Haitian Creole and French, with an emphasis Haitian Creole to support literacy acquisition. As students progress through elementary school, French instruction increases, to prepare students for national exams that are in French. Beyond core educational subjects, students also learn about the importance of physical health to promote well-being and reduce risk of illness.</p>
            <h4>What we plan on doing</h4>
            <p>Expand one additional grade a year, accomodating 20 new students and the necessary classrooms to eventually grow the school to accomodate 400 students through 12 grade. The construction of 4 new classroooms on the second flooor of the first building will be built, staffed and open by October 2024.</p>
